const assert = require('assert/strict');
const fs = require('fs');
const path = require('path');
const crypto = require('crypto');
const { pathToFileURL } = require('url');

const ROOT = process.cwd();
const SOURCE_RELATIVE_PATHS = Object.freeze([
    'tables/generated/小剧场2.1.json',
    'tables/generated/纪要.json',
]);
const BUILTIN_RELATIVE_PATH = 'modules/phone-fusion/builtin-theater-template.js';
const BUILD_SCRIPT_NAME = 'scripts/build-builtin-theater-template.cjs';
const sourcePaths = SOURCE_RELATIVE_PATHS.map((relativePath) => path.join(ROOT, relativePath));
const builtinPath = path.join(ROOT, BUILTIN_RELATIVE_PATH);
const packagePath = path.join(ROOT, 'package.json');

function sha256(text) {
    return crypto.createHash('sha256').update(text).digest('hex');
}

function readJson(filePath) {
    return JSON.parse(fs.readFileSync(filePath, 'utf8'));
}

function toModuleUrl(relativePath) {
    return pathToFileURL(path.join(ROOT, relativePath)).href;
}

function getSheetKeys(template) {
    return Object.keys(template || {}).filter((key) => key.startsWith('sheet_'));
}

function cloneJson(value) {
    return JSON.parse(JSON.stringify(value));
}

function assertHeaderIncludes(template, sheetKey, headers) {
    const actualHeaders = Array.isArray(template?.[sheetKey]?.content?.[0]) ? template[sheetKey].content[0] : [];
    headers.forEach((header) => {
        assert.ok(actualHeaders.includes(header), `${sheetKey} 缺少字段 ${header}`);
    });
}

function buildExpectedTemplate(sourceTemplates) {
    const [theaterTemplate, summaryTemplate] = sourceTemplates;
    assert.deepEqual(summaryTemplate.mate, theaterTemplate.mate, '双源 mate 必须一致，禁止 silent override');
    const merged = { mate: cloneJson(theaterTemplate.mate) };
    const seenNames = new Set();
    const theaterSheetKeys = getSheetKeys(theaterTemplate);
    for (const key of theaterSheetKeys) {
        const name = String(theaterTemplate[key]?.name || '').trim();
        assert.ok(name, `${key} 缺少 sheet name`);
        assert.ok(!seenNames.has(name), `sheet name 冲突：${name}`);
        seenNames.add(name);
        merged[key] = cloneJson(theaterTemplate[key]);
    }


    const maxOrderNo = theaterSheetKeys.reduce((max, key) => {
        const orderNo = theaterTemplate[key]?.orderNo;
        return Number.isFinite(orderNo) ? Math.max(max, orderNo) : max;
    }, -1);

    getSheetKeys(summaryTemplate).forEach((key, index) => {
        assert.ok(!Object.prototype.hasOwnProperty.call(merged, key), `sheet key 冲突：${key}`);
        const name = String(summaryTemplate[key]?.name || '').trim();
        assert.ok(name, `${key} 缺少 sheet name`);
        assert.ok(!seenNames.has(name), `sheet name 冲突：${name}`);
        seenNames.add(name);
        merged[key] = cloneJson(summaryTemplate[key]);
        merged[key].orderNo = maxOrderNo + index + 1;
    });

    return merged;
}

function createCompositeHash(sourceTexts, sourceHashes, template) {
    const payload = {
        sources: SOURCE_RELATIVE_PATHS.map((relativePath, index) => ({
            path: relativePath,
            sha256: sourceHashes[index],
        })),
        templateSha256: sha256(JSON.stringify(template)),
    };
    return sha256(JSON.stringify(payload));
}

async function main() {
    sourcePaths.forEach((sourcePath, index) => {
        assert.ok(fs.existsSync(sourcePath), `缺少源模板：${SOURCE_RELATIVE_PATHS[index]}`);
    });
    assert.ok(fs.existsSync(builtinPath), `缺少内置模板模块：${BUILTIN_RELATIVE_PATH}`);

    const sourceTexts = sourcePaths.map((sourcePath) => fs.readFileSync(sourcePath, 'utf8'));
    const sourceTemplates = sourceTexts.map((text) => JSON.parse(text));
    const sourceHashes = sourceTexts.map(sha256);
    const expectedTemplate = buildExpectedTemplate(sourceTemplates);
    const expectedHash = createCompositeHash(sourceTexts, sourceHashes, expectedTemplate);


    const builtinText = fs.readFileSync(builtinPath, 'utf8');
    const packageJson = readJson(packagePath);

    sourceTemplates.forEach((template, index) => {
        assert.equal(template?.mate?.type, 'chatSheets', `${SOURCE_RELATIVE_PATHS[index]} mate.type 必须是 chatSheets`);
    });
    assert.ok(String(packageJson.scripts?.['tables:build'] || '').includes(BUILD_SCRIPT_NAME), 'package.json tables:build 未接入 builtin 生成脚本');
    assert.ok(!/import\s+.*(?:小剧场2\.1|纪要)\.json/.test(builtinText), 'runtime builtin 模块不得 import 中文 JSON 路径');

    assert.match(builtinText, /This file is generated by scripts\/build-builtin-theater-template\.cjs/);
    assert.match(builtinText, /Do not edit manually/);
    assert.match(builtinText, new RegExp(`BUILTIN_THEATER_TEMPLATE_SHA256 = '${expectedHash}'`));

    const builtinModule = await import(`${toModuleUrl(BUILTIN_RELATIVE_PATH)}?freshness=${Date.now()}`);
    assert.equal(builtinModule.BUILTIN_THEATER_TEMPLATE_SOURCE_PATH, SOURCE_RELATIVE_PATHS.join(' + '));
    assert.deepEqual(builtinModule.BUILTIN_THEATER_TEMPLATE_SOURCE_PATHS, SOURCE_RELATIVE_PATHS);
    assert.deepEqual(builtinModule.BUILTIN_THEATER_TEMPLATE_SOURCE_HASHES, Object.fromEntries(SOURCE_RELATIVE_PATHS.map((relativePath, index) => [relativePath, sourceHashes[index]])));
    assert.equal(builtinModule.BUILTIN_THEATER_TEMPLATE_SHA256, expectedHash);
    assert.equal(builtinModule.BUILTIN_THEATER_TEMPLATE?.mate?.type, 'chatSheets');

    const builtinSheetKeys = getSheetKeys(builtinModule.BUILTIN_THEATER_TEMPLATE);
    const expectedSheetKeys = getSheetKeys(expectedTemplate);
    assert.deepEqual(builtinSheetKeys.sort(), expectedSheetKeys.sort(), 'builtin sheet 列表必须与双源合成模板一致');
    assert.ok(builtinSheetKeys.length >= 7, '内置小剧场+纪要表至少应包含 7 张 sheet');
    assert.ok(builtinSheetKeys.includes('sheet_summary'), '内置小剧场+纪要表必须包含 sheet_summary');
    assert.equal(builtinModule.BUILTIN_THEATER_TEMPLATE.sheet_summary?.name, '纪要表');

    assert.equal(typeof builtinModule.createBuiltinTheaterTemplate, 'function');
    const cloned = builtinModule.createBuiltinTheaterTemplate();
    assert.notEqual(cloned, builtinModule.BUILTIN_THEATER_TEMPLATE, 'createBuiltinTheaterTemplate 必须返回新对象');
    assert.deepEqual(cloned, expectedTemplate, 'clone factory 输出必须与双源合成模板一致');

    assertHeaderIncludes(cloned, 'sheet_square_posts', ['图片描述', '视频描述', '评论串']);
    assertHeaderIncludes(cloned, 'sheet_forum_threads', ['评论串']);
    assertHeaderIncludes(cloned, 'sheet_livestream_rooms', ['剧情弹幕串', '推角弹幕串', '对线弹幕串']);
    assertHeaderIncludes(cloned, 'sheet_summary', ['编码索引', '时间跨度', '概览', '纪要']);

    console.log('[fusion-builtin-theater-template-freshness-check] 检查通过');
    console.log(`- OK | ${BUILTIN_RELATIVE_PATH} | hash 与双源合成模板一致：${expectedHash}`);
    console.log(`- OK | ${BUILTIN_RELATIVE_PATH} | 导出 source paths/source hashes/template/clone factory 且结构有效`);
    console.log(`- OK | ${BUILTIN_RELATIVE_PATH} | 同时包含小剧场 sheet 与 sheet_summary/纪要表`);
    console.log(`- OK | package.json | tables:build 已接入 ${BUILD_SCRIPT_NAME}`);
}

main().catch((error) => {
    console.error('[fusion-builtin-theater-template-freshness-check] 检查失败：');
    console.error(error);
    process.exitCode = 1;
});
